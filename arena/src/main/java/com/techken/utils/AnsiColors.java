package com.techken.utils;

public class AnsiColors {
    // Para ni sa colors sa font sa menus, user interfaces, etc.
    
    public static final String RESET       = "\u001B[0m";
    public static final String BOLD        = "\u001B[1m";

    // Red colors
    public static final String RED         = "\u001B[31m";
    public static final String RED_BRIGHT  = "\u001B[91m";

    // Yellow colors
    public static final String YELLOW      = "\u001B[33m";
    public static final String YELLOW_BRIGHT = "\u001B[93m";

    // Green colors
    public static final String GREEN       = "\u001B[32m";

    // White Colors
    public static final String WHITE       = "\u001B[97m";
    public static final String WHITE_DIM   = "\u001B[37m"; 

}
